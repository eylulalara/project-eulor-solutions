# Problem 17 - Number Letter Counts
# How many letters to write 1 to 1000?
# ─────────────────────────────────────────
def problem_17():
    ones = [0,3,3,5,4,4,3,5,5,4,3,6,6,8,8,7,7,9,8,8]
    tens = [0,0,6,6,5,5,5,7,6,6]
    total = 0
    for n in range(1, 1001):
        if n == 1000:
            total += 11  # "onethousand"
        elif n >= 100:
            h = n // 100
            rem = n % 100
            total += ones[h] + 7  # "hundred"
            if rem > 0:
                total += 3  # "and"
                if rem < 20:
                    total += ones[rem]
                else:
                    total += tens[rem // 10] + ones[rem % 10]
        elif n >= 20:
            total += tens[n // 10] + ones[n % 10]
        else:
            total += ones[n]
    return total


