# Problem 14 - Longest Collatz Sequence
# Which starting number under 1 million produces the longest chain?
# ─────────────────────────────────────────
def problem_14():
    def collatz_len(n, memo={}):
        if n in memo: return memo[n]
        if n == 1: return 1
        length = 1 + collatz_len(n // 2 if n % 2 == 0 else 3 * n + 1)
        memo[n] = length
        return length
    return max(range(1, 1_000_000), key=collatz_len)


