# Problem 12 - Highly Divisible Triangular Number
# First triangle number with more than 500 divisors.
# ─────────────────────────────────────────
def problem_12():
    def count_divisors(n):
        count = 0
        for i in range(1, int(n**0.5) + 1):
            if n % i == 0:
                count += 2 if i != n // i else 1
        return count
    n, tri = 1, 1
    while count_divisors(tri) <= 500:
        n += 1
        tri += n
    return tri


