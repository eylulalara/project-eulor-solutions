# Problem 3 - Largest Prime Factor
# Largest prime factor of 600851475143.
# ─────────────────────────────────────────
def problem_03():
    n = 600851475143
    factor = 2
    largest = 1
    while factor * factor <= n:
        while n % factor == 0:
            largest = factor
            n //= factor
        factor += 1
    return n if n > 1 else largest


# ─────────────────────────────────────────
# Problem 4 - Largest Palindrome Product
# Largest palindrome made from the product of two 3-digit numbers.
# ─────────────────────────────────────────
def problem_04():
    largest = 0
    for i in range(999, 99, -1):
        for j in range(i, 99, -1):
            product = i * j
            if product <= largest:
                break
            if str(product) == str(product)[::-1]:
                largest = product
    return largest


# ─────────────────────────────────────────
# Problem 5 - Smallest Multiple
# Smallest positive number evenly divisible by all of 1 to 20.
# ─────────────────────────────────────────
def problem_05():
    from math import gcd
    result = 1
    for i in range(1, 21):
        result = result * i // gcd(result, i)
    return result


# ─────────────────────────────────────────
# Problem 6 - Sum Square Difference
# Difference between sum of squares and square of sum (1..100).
# ─────────────────────────────────────────
def problem_06():
    n = 100
    sum_sq = sum(i * i for i in range(1, n + 1))
    sq_sum = sum(range(1, n + 1)) ** 2
    return sq_sum - sum_sq


