# Problem 9 - Special Pythagorean Triplet
# Find a*b*c where a+b+c=1000 and a²+b²=c².
# ─────────────────────────────────────────
def problem_09():
    for a in range(1, 1000):
        for b in range(a, 1000):
            c = 1000 - a - b
            if c > 0 and a*a + b*b == c*c:
                return a * b * c


