# Problem 7 - 10001st Prime
# ─────────────────────────────────────────
def problem_07():
    def is_prime(n):
        if n < 2: return False
        if n == 2: return True
        if n % 2 == 0: return False
        for i in range(3, int(n**0.5) + 1, 2):
            if n % i == 0: return False
        return True
    count, num = 0, 1
    while count < 10001:
        num += 1
        if is_prime(num):
            count += 1
    return num


