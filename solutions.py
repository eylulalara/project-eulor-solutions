# Problem 10 - Summation of Primes
# Sum of all primes below 2 million.
# ─────────────────────────────────────────
def problem_10():
    limit = 2_000_000
    sieve = [True] * limit
    sieve[0] = sieve[1] = False
    for i in range(2, int(limit**0.5) + 1):
        if sieve[i]:
            for j in range(i*i, limit, i):
                sieve[j] = False
    return sum(i for i, v in enumerate(sieve) if v)


